    void cargarDatos() {
        modelo.setRowCount(0);
        try ( Connection connection = DbConnection.getConnection()) {
            // Consulta SQL con JOIN para obtener el nombre del proveedor
            String sql = "SELECT p.idProducto, p.nombre AS productoNombre, p.precio, p.idProveedor, pr.nombre AS proveedorNombre "
                    + "FROM producto p "
                    + "JOIN proveedor pr ON p.idProveedor = pr.idProveedor";

            try ( PreparedStatement statement = connection.prepareStatement(sql);  ResultSet resultSet = statement.executeQuery()) {
                while (resultSet.next()) {
                    modelo.addRow(new Object[]{
                        resultSet.getInt("idProducto"),
                        resultSet.getString("productoNombre"),
                        resultSet.getDouble("precio"),
                        resultSet.getInt("idProveedor"),
                        resultSet.getString("proveedorNombre") // Agregar el nombre del proveedor
                    });
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            // Mostrar mensaje de error
        }
    }

    private void cargarProveedor() {
        try ( Connection connection = DbConnection.getConnection()) {
            String sql = "SELECT idProveedor, nombre FROM proveedor";
            try ( PreparedStatement statement = connection.prepareStatement(sql);  ResultSet resultSet = statement.executeQuery()) {
                while (resultSet.next()) {
                    combobox_proveedores.addItem(resultSet.getInt("idProveedor") + " - " + resultSet.getString("nombre"));
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            // Mostrar mensaje de error
        }
    }

    private void cargarCamposDesdeTabla(int row) {

        text_nombre.setText(modelo.getValueAt(row, 1).toString());
        text_precio.setText(modelo.getValueAt(row, 2).toString());
        String proveedorNombre = modelo.getValueAt(row, 3) + " " + modelo.getValueAt(row, 4);
        String proveedorId = modelo.getValueAt(row, 3).toString();

        // Establecer el seleccionado en el JComboBox
        for (int i = 0; i < combobox_proveedores.getItemCount(); i++) {
            if (combobox_proveedores.getItemAt(i).startsWith(proveedorId)) {
                combobox_proveedores.setSelectedIndex(i);
                break;
            }
        }

        btn_agregar.setEnabled(false);
        editando = true;
    }

    private void limpiarCampos() {
        text_nombre.setText("");
        text_precio.setText("");

        combobox_proveedores.setSelectedIndex(-1);

        btn_agregar.setEnabled(true);
        editando = false;
    }

