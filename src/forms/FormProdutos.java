    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed

        String nombre = text_nombre.getText();
        String precio = text_precio.getText();

        if (nombre.isEmpty() || precio.isEmpty() || combobox_proveedores.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try ( Connection connection = DbConnection.getConnection()) {
            String sql = "INSERT INTO producto (nombre, precio, idProveedor) VALUES (?, ?, ?)";

            PreparedStatement statement = connection.prepareStatement(sql);

            statement.setString(1, nombre);
            statement.setDouble(2, Double.parseDouble(precio));
            String selectedItem = (String) combobox_proveedores.getSelectedItem();
            int idProveedor = Integer.parseInt(selectedItem.split(" - ")[0]);
            statement.setInt(3, idProveedor);

            int rowsInserted = statement.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Producto agregado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                cargarDatos();
                limpiarCampos();

            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al agregar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void btn_aliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aliminarActionPerformed

        int selectedRow = table_productos.getSelectedRow();
        if (selectedRow >= 0) {
            int id = (int) modelo.getValueAt(selectedRow, 0);

            int confirm = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas eliminar este producto?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try ( Connection conn = DbConnection.getConnection()) {
                    String sql = "DELETE FROM Producto WHERE IdProducto = ?";
                    PreparedStatement statement = conn.prepareStatement(sql);
                    statement.setInt(1, id);
                    statement.executeUpdate();

                    cargarDatos();
                    limpiarCampos();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona un producto para eliminar.");
        }
    }//GEN-LAST:event_btn_aliminarActionPerformed

    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed

        int selectedRow = table_productos.getSelectedRow();
        if (selectedRow >= 0) {
            int idProducto = (int) modelo.getValueAt(selectedRow, 0);

            String nombre = text_nombre.getText();
            double precio = Double.parseDouble(text_precio.getText());
            String selectedItem = (String) combobox_proveedores.getSelectedItem();
            int idProveedor = Integer.parseInt(selectedItem.split(" - ")[0]);

            try ( Connection conn = DbConnection.getConnection()) {
                String sql = "UPDATE Producto SET Nombre = ?, Precio = ?, IdProveedor = ? WHERE IdProducto = ?";
                PreparedStatement statement = conn.prepareStatement(sql);
                statement.setString(1, nombre);
                statement.setDouble(2, precio);
                statement.setInt(3, idProveedor);
                statement.setInt(4, idProducto);

                
                int rowsUpdated = statement.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "El registro se actualizó correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                    cargarDatos();
                    limpiarCampos();

                }

               
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_editarActionPerformed

