    private void agregarEfectoClick(JPanel panel) {
        panel.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                // Cambiar el color de fondo para dar un efecto de clic
                panel.setBackground(Color.darkGray);

                // Puedes agregar un peque√±o retardo para simular el efecto de clic
                new java.util.Timer().schedule(new java.util.TimerTask() {
                    @Override
                    public void run() {
                        panel.setBackground(null); // Restablecer el color original
                    }
                }, 200); // 200 ms de retardo
            }

            @Override
            public void mouseEntered(MouseEvent e) {
                // Cambiar el color al pasar el mouse sobre el panel
                panel.setBackground(Color.lightGray);
            }

            @Override
            public void mouseExited(MouseEvent e) {
                // Restablecer el color original cuando el mouse sale del panel
                panel.setBackground(null);
            }
        });
    }

